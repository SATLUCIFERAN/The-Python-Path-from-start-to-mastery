
fruits = ["apple", "banana", "cherry", "date"]

print(fruits[1:3])   # ['banana', 'cherry']
print(fruits[:2])    # ['apple', 'banana']
print(fruits[::2])   # ['apple', 'cherry']
print(fruits[-1])    # 'date'
print(fruits[-2])    # 'cherry'
print(fruits[:])     # ['apple', 'banana', 'cherry', 'date']




